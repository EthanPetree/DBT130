-- get product information along with the number of times 
-- each product has been ordered
-- and the total quantity ordered
-- prod id, prod name, TotalOrderQuantity, TotalProductQuantity
SELECT p.ProductID, p.ProductName, COUNT(od.OrderID) as TotalOrderQuantity, SUM(od.Quantity) as TotalProductQuantity
FROM Products as p
LEFT OUTER JOIN OrderDetails as od
    ON p.ProductID = od.ProductID
-- LEFT OUTER JOIN Orders as o
--     ON od.OrderID = o.OrderID
GROUP BY p.ProductID, p.ProductName
ORDER BY TotalProductQuantity DESC


-- COUNT
