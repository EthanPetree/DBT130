-- Numeric Aggregation
SELECT COUNT(Quantity) as Count,
SUM(Quantity) as [Sum of Quantity]
FROM OrderDetails

-- TOP
SELECT TOP(5) *
FROM OrderDetails
ORDER BY Quantity DESC
-- multiple dupicate quantities

SELECT TOP(5) WITH TIES *
FROM OrderDetails
ORDER BY Quantity DESC

-- Order Details Stats
SELECT OrderID,
COUNT(Quantity) AS [Number of Rows],
SUM(Quantity) as [Total Quantity Ordered],
AVG(Quantity) as [Average QTY per Line Item],
MIN(Quantity) as [Smallest],
MAX(Quantity) as [Largest]
FROM OrderDetails
GROUP BY OrderID
ORDER BY [Largest] DESC

-- calculate the first and last order date for each customer
SELECT c.CustomerID, c.CompanyName,
MIN(o.OrderDate) as [FirstOrder], MAX(o.OrderDate) as [LastOrder]
FROM Customers as c
INNER JOIN Orders as o
    ON c.CustomerID = o.CustomerID
GROUP BY c.CustomerID, c.CompanyName

-- calculate total revenue generated by each category in the Products table
-- sort by descending revenue
SELECT cat.CategoryName,
    SUM((od.UnitPrice * od.Quantity) * (1 - od.Discount)) as TotalRevenue
FROM Categories as cat
JOIN Products as p
    ON cat.CategoryID = p.CategoryID
JOIN OrderDetails as od
    ON p.ProductID = od.ProductID
GROUP BY cat.CategoryName
ORDER BY TotalRevenue DESC

--Fix Total Revenue to Currency
SELECT cat.CategoryName,
    FORMAT(SUM((od.UnitPrice * od.Quantity) * (1 - od.Discount)), 'C') as TotalRevenue
FROM Categories as cat
JOIN Products as p
    ON cat.CategoryID = p.CategoryID
JOIN OrderDetails as od
    ON p.ProductID = od.ProductID
GROUP BY cat.CategoryName
ORDER BY TotalRevenue DESC
